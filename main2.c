void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin){
	if (GPIO_Pin == BTN_Pin){
		if (devState == CONTROL){ //Si ya llegÃ³ al ultimo estado, vuelve al estado 0 IDLE.
			devState = IDLE;
		} else {
			devState++; //Aqui va cambiando el estado de manera secuencial,0, 1, 2, 3... 0, 1,...
		}
	}
}

