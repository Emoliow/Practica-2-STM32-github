void separar(){
	for(i=0;i<150;i++) {
		LDR1[i]=*ptr1;
		LDR2[i]=*ptr2;
		ptr1+=2;
		ptr2+=2;
	}

}

void valoresVoltajeLDR1(){
	for(i=0;i<150;i++) {
		valorDigital= *ptrLDR1;
		valorVolt= (valorDigital*ADC_VREF)/pow(2,16);
		LDR1_V[i]=valorVolt;
		ptrLDR1++;
	}
}

void valoresVoltajeLDR2(){
	for(i=0;i<150;i++) {
		valorDigital= *ptrLDR2;
		valorVolt= (valorDigital*ADC_VREF)/pow(2,16);
		LDR2_V[i]=valorVolt;
		ptrLDR2++;
	}
}

